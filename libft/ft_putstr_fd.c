#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	i;

	if (!s)
		return ;
	i = 0;
	while (*(s + i))
		i++;
	if (fd == 2)
		write(2, "\e[1;31m",7);
	write(fd, s, i);
	if (fd == 2)
		write(2, "\e[0m",7);
}
